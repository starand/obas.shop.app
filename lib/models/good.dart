class Good {
  final int id;
  final String code;
  final String name;
  final double price;
  final List<String> images;
  final bool present;
  final double discount;
  final String producer;
  final int producerId;
  final String season;
  final int seasonId;
  final String uniqId;

  Good({
    this.id,
    this.code,
    this.name,
    this.price,
    this.images,
    this.present,
    this.discount,
    this.producer,
    this.producerId,
    this.season,
    this.seasonId,
    this.uniqId,
  });
}
